     *
     * @return \Illuminate\Http\Response
     */
    public function create(Request $request)
    {
        $image = $request->get('photo');

        $image = ($image)? Storage::url( $request->get('photo')) : false;

        return view('file-storage.login', ['photo' => $image]);

    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $file = $request->file('upload');

        if ($file->isFile()) {
            $filename = now()->format('His') . 'BukuSaya.'. $file->getClientOriginalExtension();

            $path = $file->storeAs(
                'book',
                $filename,
                'public'
            );

            return redirect(route('fileUpload', ['photo' => $path]));
        }

    }

